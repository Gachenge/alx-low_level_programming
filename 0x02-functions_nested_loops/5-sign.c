#include "main.h"

/**
 * print_sign - check if positive or negative
 *
 * @n: input assign n
 *
 * Return: zilch
 */

int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	return (0);
}
