#include "3-calc.h"

/**
 * op_add - add two
 * @a: the first
 * @b: the second
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract
 * @a: the first
 * @b: the sec
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply
 * @a: the first
 * @b: the second
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide
 * @a: the first
 * @b: the second
 * Return: divided
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder
 * @a: the first
 * @b: the second
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
